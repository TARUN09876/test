// Error Callback
void HAL_I2C_ErrorCallback(I2C_HandleTypeDef *hi2c) {
    if (HAL_I2C_GetError(hi2c) == HAL_I2C_ERROR_AF) {
        printf("I2C Error: Acknowledge Failure\n");
    } else if (HAL_I2C_GetError(hi2c) == HAL_I2C_ERROR_DMA) {
        printf("I2C Error: DMA Transfer\n");
    } else {
        printf("I2C Error: Unknown\n");
    }
}



