// I2C Write to Multiple Slave Devices
void I2C_WriteToMultipleSlaves(void) {
    uint8_t slaveAddresses[] = {0x50, 0x68, 0x72};
    uint8_t dataToSend[2] = {0xAA, 0xBB};

    for (int i = 0; i < sizeof(slaveAddresses); i++) {
        if (HAL_I2C_Master_Transmit(&hi2c1, slaveAddresses[i], dataToSend, 2, I2C_TIMEOUT) != HAL_OK) {
            Error_Handler();
        }
    }
}

// I2C Large Data Transfer using DMA
void I2C_LargeDataTransfer(uint8_t deviceAddress) {
    uint8_t largeBuffer[1024];
    for (int i = 0; i < 1024; i++) {
        largeBuffer[i] = i & 0xFF;
    }

    if (HAL_I2C_Master_Transmit_DMA(&hi2c1, deviceAddress, largeBuffer, 1024) != HAL_OK) {
        Error_Handler();
    }
}

